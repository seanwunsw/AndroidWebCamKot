@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun AlertDialogExample(
    onDismissRequest: () -> Unit,
    onConfirmation: () -> Unit,
    dialogTitle: String,
    dialogText: String,
    icon: ImageVector,
) {
    AlertDialog(
        icon = {
            Icon(icon, contentDescription = "Example Icon")
        },
        title = {
            Text(text = dialogTitle)
        },
        text = {
            Text(text = dialogText)
        },
        onDismissRequest = {
            onDismissRequest()
        },
        confirmButton = {
            TextButton(
                onClick = {
                    onConfirmation()
                }
            ) {
                Text("Confirm")
            }
        },
        dismissButton = {
            TextButton(
                onClick = {
                    onDismissRequest()
                }
            ) {
                Text("Dismiss")
            }
        }
    )
}

@Composable
fun PermissionScreen(
    targetPermission:() -> Unit
) {
    val activity = (LocalContext.current as? Activity)
    AlertDialogExample(
        onDismissRequest = {activity?.finish()},
        onConfirmation = {targetPermission()},
        dialogTitle = "Camera Permission required",
        dialogText = "The camera permission is essiential for this app to work properly",
        icon = Icons.TwoTone.Info
    )



}





